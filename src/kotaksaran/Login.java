    public void cek(){
        if(getNis().equals("") || getSandi().equals("")){
            db.display(0, "Pastikan Seluruh Field Telah Terisi!");
        }
        else{    
            int hasil = db.login(getNis(), getSandi());
            if(hasil<1){
                this.setVisible(true);
            }
            else{
                DataSaran ds = new DataSaran(getNis());
                this.dispose();
                ds.setVisible(true);
            }
        }
    }
    
